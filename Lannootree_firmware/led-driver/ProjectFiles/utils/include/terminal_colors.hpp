#pragma once

/**
 * @brief 
 * 
 * Some macros used to print to terminal to set different colors
 * Where F_COLOR are forground colors,
 * and B_COLOR are background colors.
 * 
 */

#define RESET_COLOR "\u001b[0m"

// Background colors
#define B_BLACK   "\u001b[40m"
#define B_RED     "\u001b[41m"
#define B_GREEN   "\u001b[42m"
#define B_YELLOW  "\u001b[43m"
#define B_BLUE    "\u001b[44m"
#define B_MAGENTA "\u001b[45m"
#define B_CYAN    "\u001b[46m"
#define B_WHITE   "\u001b[47m"

// Foreground colors
#define F_BLACK   "\u001b[30m"
#define F_RED     "\u001b[31m"
#define F_GREEN   "\u001b[32m"
#define F_YELLOW  "\u001b[33m"
#define F_BLUE    "\u001b[34m"
#define F_MAGENTA "\u001b[35m"
#define F_CYAN    "\u001b[36m"
#define F_WHITE   "\u001b[37m"
